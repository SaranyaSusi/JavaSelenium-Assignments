package week1.day1;

public class FactorialNumber
{
	 public static void main(String[] args) 
	 {
		 int a = 5;
		 int fact = 1; 
		 for (int i=1; i<=a; i++) 
		 {
			 fact=fact*i;
		 }
		 System.out.println("The factorial number of 5 is : "+fact );
	}
}


